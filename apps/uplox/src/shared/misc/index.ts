export const BYTE_GB = BYTE_MB * 1024;
export const BYTE_TB = BYTE_GB * 1024;
export const BYTE_PB = BYTE_TB * 1024;
export const BYTE_EB = BYTE_PB * 1024;
export const BYTE_ZB = BYTE_EB * 1024;
